#!/bin/bash

# ==========================================
# CV 推理平台 一键启动脚本
# ==========================================

echo "🚀 准备启动 CV 智能推理平台..."

# 🚨 核心魔法：捕获 Ctrl+C (SIGINT) 信号
# 当你按下 Ctrl+C 时，这行代码会把当前脚本产生的所有后台子进程全部杀掉
trap 'echo -e "\n🛑 收到关闭指令，正在安全停止所有服务..."; kill 0; exit 1' SIGINT

# ------------------------------------------
# 1. 启动 FastAPI 后端
# ------------------------------------------
echo "📦 正在启动后端服务 (FastAPI + C++ Engine)..."
cd backend || exit

# 如果你使用了虚拟环境 (如 conda 或 venv)，请取消注释并修改下面这行：
# source /path/to/your/venv/bin/activate 

# 将后端丢入后台运行 (&)
uvicorn app.main:app --host 0.0.0.0 --port 3061 --reload &
BACKEND_PID=$!
cd ..

# ------------------------------------------
# 2. 启动 React 前端
# ------------------------------------------
echo "🎨 正在启动前端服务 (React + Vite)..."
cd frontend || exit

# 将前端丢入后台运行 (&)
npm run dev &
FRONTEND_PID=$!
cd ..

# ------------------------------------------
# 3. 挂起主进程
# ------------------------------------------
echo "=========================================="
echo "✅ 所有服务已成功发射！"
echo "👉 前端地址: http://localhost:3060"
echo "👉 后端地址: http://localhost:3061/docs"
echo "💡 随时按下 [Ctrl + C] 即可一键安全关闭所有服务"
echo "=========================================="

# 挂起脚本，等待后台进程，防止脚本瞬间执行完毕退出
wait